      <h2 style="color:white;">Anatomises</h2>
    </div>
    <div class="modaladvice-body">
      <p>Deconstructs an organisation’s value chain into its component parts (i.e. value drivers) to examine them in more detail.</p>
      <p> -	Deconstruct an End to End SIPOC, Value Chain or Business Model Canvas of the key suppliers, inputs, processes, outputs and customers of your organisation and how they drive value.</P>
      <p> -	Analyse the value added at each stage and how it contributes to overall shareholder margin/value</P>
      <p> -	Identify any bottlenecks, throughput challenges and sustainability of the various parts of the value chain.</P>
